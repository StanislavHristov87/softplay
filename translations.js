// Language management
let currentLanguage = localStorage.getItem('language') || 'bg';

function setLanguage(lang) {
    currentLanguage = lang;
    localStorage.setItem('language', lang);
    document.documentElement.lang = lang;
    updatePageContent();
    updateSEOMetaTags(lang);
}

function getTranslation(key) {
    const keys = key.split('.');
    let value = translations[currentLanguage];
    for (const k of keys) {
        value = value?.[k];
    }
    return value || key;
}

function updatePageContent() {
    // Update all elements with data-translate attribute
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        const translation = getTranslation(key);
        if (element.tagName === 'OPTION') {
            element.textContent = translation;
        } else {
            element.textContent = translation;
        }
    });
    
    // Update all elements with data-translate-placeholder attribute
    document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
        const key = element.getAttribute('data-translate-placeholder');
        const translation = getTranslation(key);
        element.placeholder = translation;
    });
    
    // Update language switcher active state
    document.querySelectorAll('.lang-switcher').forEach(btn => {
        if (btn.dataset.lang === currentLanguage) {
            btn.classList.add('active');
        } else {
            btn.classList.remove('active');
        }
    });
}

// Update SEO Meta Tags
function updateSEOMetaTags(lang) {
    // Get current page identifier
    const path = window.location.pathname;
    let pageKey = 'home';
    
    if (path.includes('services.html')) pageKey = 'services';
    else if (path.includes('gallery.html')) pageKey = 'gallery';
    else if (path.includes('products.html')) pageKey = 'products';
    else if (path.includes('about.html')) pageKey = 'about';
    else if (path.includes('tips.html')) pageKey = 'tips';
    else if (path.includes('contact.html')) pageKey = 'contact';
    
    const seo = seoMeta[lang] && seoMeta[lang][pageKey] ? seoMeta[lang][pageKey] : seoMeta['bg'][pageKey];
    
    if (seo) {
        // Update title
        if (document.querySelector('title')) {
            document.querySelector('title').textContent = seo.title;
        }
        
        // Update or create meta description
        let metaDesc = document.querySelector('meta[name="description"]');
        if (!metaDesc) {
            metaDesc = document.createElement('meta');
            metaDesc.setAttribute('name', 'description');
            document.head.appendChild(metaDesc);
        }
        metaDesc.setAttribute('content', seo.description);
        
        // Update or create meta keywords
        let metaKeywords = document.querySelector('meta[name="keywords"]');
        if (!metaKeywords) {
            metaKeywords = document.createElement('meta');
            metaKeywords.setAttribute('name', 'keywords');
            document.head.appendChild(metaKeywords);
        }
        metaKeywords.setAttribute('content', seo.keywords);
        
        // Update Open Graph tags
        updateMetaProperty('og:title', seo.title);
        updateMetaProperty('og:description', seo.description);
        updateMetaProperty('og:locale', lang === 'bg' ? 'bg_BG' : 'en_GB');
        
        // Update Twitter tags
        updateMetaProperty('twitter:title', seo.title);
        updateMetaProperty('twitter:description', seo.description);
    }
}

function updateMetaProperty(property, content) {
    let meta = document.querySelector(`meta[property="${property}"]`);
    if (!meta) {
        meta = document.createElement('meta');
        meta.setAttribute('property', property);
        document.head.appendChild(meta);
    }
    meta.setAttribute('content', content);
}

